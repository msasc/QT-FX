SELECT MSALESORIG.CFAMILY AS CFAMILY,
       FAMILIES.DFAMILY AS DFAMILY,
       MSALESORIG.CARTDICT AS CARTDICT,
       (SUM(MSALESORIG.QSALES)) AS QSALES,
       (SUM(MSALESORIG.MNETSALES + MSALESORIG.MSALESD)) AS MSALEST,
       (SUM(MSALESORIG.MCOST + MSALESORIG.MCOSTD)) AS MCOSTT,
       (SUM(MSALESORIG.MCOSTO + MSALESORIG.MCOSTDO)) AS MCOSTOT,
       (SUM(MSALESORIG.MNETSALES + MSALESORIG.MSALESD - MSALESORIG.MCOST - MSALESORIG.MCOSTD - MSALESORIG.MCOSTO - MSALESORIG.MCOSTDO)) AS MMARGIN
  FROM MSALESORIG,
       ARTICLES,
       BUSINESSES,
       TPIECES,
       UNITS,
       SETS,
       ARTICLES_FAM,
       FAMILIES,
       COMPANY_CUSTOMERS,
       COMPANY_SCHANNELS,
       STYLES
 WHERE MSALESORIG.CARTDICT = ARTICLES.CARTICLE(+)
   AND MSALESORIG.CBUSINESS = BUSINESSES.CBUSINESS(+)
   AND MSALESORIG.CPTYPE = TPIECES.CPTYPE(+)
   AND TPIECES.CUNIT = UNITS.CUNIT(+)
   AND MSALESORIG.CBUSINESS = SETS.CBUSINESS(+)
   AND MSALESORIG.CBRAND = SETS.CBRAND(+)
   AND MSALESORIG.CSET = SETS.CSET(+)
   AND MSALESORIG.CCOMPANY = ARTICLES_FAM.CCOMPANY(+)
   AND MSALESORIG.CARTDICT = ARTICLES_FAM.CARTICLE(+)
   AND ARTICLES_FAM.CCOMPANY = FAMILIES.CCOMPANY(+)
   AND ARTICLES_FAM.CFAMILY = FAMILIES.CFAMILY(+)
   AND MSALESORIG.CCOMPANY = COMPANY_CUSTOMERS.CCOMPANY(+)
   AND MSALESORIG.CCUSTOMER = COMPANY_CUSTOMERS.CCUSTOMER(+)
   AND COMPANY_CUSTOMERS.CCOMPANY = COMPANY_SCHANNELS.CCOMPANY(+)
   AND COMPANY_CUSTOMERS.CSCHANNEL = COMPANY_SCHANNELS.CSCHANNEL(+)
   AND ARTICLES.CSTYLE = STYLES.CSTYLE(+)
   AND MSALESORIG.CCOMPANY = 'LII'
   AND MSALESORIG.NYEAR = 2017
 GROUP BY MSALESORIG.CFAMILY, FAMILIES.DFAMILY, MSALESORIG.CARTDICT
 ORDER BY MSALESORIG.CFAMILY, FAMILIES.DFAMILY, MSALESORIG.CARTDICT



